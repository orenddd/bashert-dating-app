export function mockGetDiscoverProfiles(currentUserId: string, filters: Partial<SearchFilters> = {}): DbProfile[] {
  return MOCK_PROFILES.filter(p => {
    if (p.user_id === currentUserId) return false
    const age = calcAge(p.date_of_birth)
    if (filters.age_min && age < filters.age_min) return false
    if (filters.age_max && age > filters.age_max) return false
    if (filters.religious_levels?.length && !filters.religious_levels.includes(p.religious_level)) return false
    if (filters.community_backgrounds?.length && !filters.community_backgrounds.includes(p.community_background)) return false
    if (filters.shomer_shabbat_only && !p.shomer_shabbat) return false
    if (filters.verified_only && !p.is_verified) return false
    return true
  })
}

